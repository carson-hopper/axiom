package com.axiommc.api.chat;

/**
 * Text formatting decorations for chat components.
 *
 * <p>These decorations can be applied to chat text to add visual styling.
 */
public enum ChatDecoration {
    /**
     * Bold text formatting.
     */
    BOLD,

    /**
     * Italic (slanted) text formatting.
     */
    ITALIC,

    /**
     * Underlined text formatting.
     */
    UNDERLINED,

    /**
     * Strikethrough text formatting.
     */
    STRIKETHROUGH,

    /**
     * Obfuscated (random character) text formatting.
     */
    OBFUSCATED
}
