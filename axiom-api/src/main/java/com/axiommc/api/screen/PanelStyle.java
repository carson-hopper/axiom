package com.axiommc.api.screen;

/**
 * Visual style for a {@link ScreenElement.Panel}.
 */
public enum PanelStyle {

    /** Dark smooth stone — good general-purpose background. */
    DARK,

    /** Tinted glass — semi-transparent, useful for overlay cards. */
    GLASS,

    /** Blackstone — slightly textured dark border. */
    BORDER,

    /** Warped planks — teal-tinted accent panel. */
    ACCENT,
}
