local function split(value, separator)
    if separator == nil then
        separator = "%s"
    end
    local result = {}
    for segment in string.gmatch(value, "([^" .. separator .. "]+)") do
        table.insert(result, segment)
    end
    return result
end

local function loadPacketIds(versionName)
    local result = {}

    local function loadJsonFile(path)
        local file = io.open(path, "r")
        if not file then return nil end
        local content = file:read("*a")
        file:close()
        return json.decode(content)
    end

    local versionPath = "../scripts/packet-ids/" .. versionName .. ".json"
    local versionData = loadJsonFile(versionPath) or {}

    local function normalize(key)
        key = key:gsub("-", "_"):upper()
        if key == "SERVERBOUND" then return "SB"
        elseif key == "CLIENTBOUND" then return "CB"
        else return key end
    end

    local function flattenTable(table, prefix)
        for key, value in pairs(table) do
            local newPrefix = prefix and (prefix .. "_" .. normalize(key)) or normalize(key)
            if type(value) == "table" then
                flattenTable(value, newPrefix)
            else
                if type(value) == "string" then
                    value = '"' .. value .. '"'
                elseif type(value) == "boolean" then
                    value = value and "1" or "0"
                end
                ::continue::
                result[#result + 1] = "PID_" .. newPrefix .. "=" .. tostring(value)
            end
        end
    end

    flattenTable(versionData, nil)
    return result
end

local function registerServerPackets()
    local files = os.matchfiles("src/Axiom/Network/Packet/**/Serverbound/**.h")

    local imports = {}
    local definitions = {}

    for _, file in ipairs(files) do
        table.insert(imports, tostring(string.gsub(file, "src/", "")))

        local relative = string.gsub(file, "src/Axiom/Network/Packet/", "")
        local parts = split(relative, "/")
        -- parts[1] = state dir (e.g. "Play")
        -- parts[2] = "Serverbound"
        -- parts[3] = filename (e.g. "KeepAlive.h")
        if #parts >= 3 then
            local className = string.gsub(parts[3], "%.h$", "") .. "Packet"
            local namespacePath = "Axiom::" .. parts[1] .. "::" .. parts[2] .. "::" .. className
            table.insert(definitions, namespacePath)
        end
    end

    return { imports, definitions }
end

local function generatePacketFactory()
    local items = registerServerPackets()
    local imports = items[1]
    local definitions = items[2]

    local headerPath = "src/Axiom/Network/Packet/RegisterPackets.generated.h"
    local sourcePath = "src/Axiom/Network/Packet/RegisterPackets.generated.cpp"

    -- Header
    local headerFile = io.open(headerPath, "w")
    headerFile:write("#pragma once\n")
    headerFile:write("#include \"Axiom/Core/Base.h\"\n\n")
    for _, import in ipairs(imports) do
        headerFile:write("#include \"" .. import .. "\"\n")
    end
    headerFile:close()

    -- Source
    local sourceFile = io.open(sourcePath, "w")
    sourceFile:write("#include \"axpch.h\"\n")
    sourceFile:write("#include \"RegisterPackets.generated.h\"\n\n")
    sourceFile:write("#include \"Axiom/Network/Packet/PacketFactory.h\"\n\n")
    sourceFile:write("namespace Axiom {\n\n")
    sourceFile:write("    void PacketFactory::RegisterAll() {\n")
    for _, definition in ipairs(definitions) do
        sourceFile:write("        RegisterDefaultParser<" .. definition .. ">();\n")
    end
    sourceFile:write("    }\n\n")
    sourceFile:write("}\n")
    sourceFile:close()
end

-- ----- Helper: add per-version defines --------------------------

local function addVersionDefine(version, name, value)
    filter("configurations:Debug_" .. version)
        defines { name .. "=" .. value }

    filter("configurations:Release_" .. version)
        defines { name .. "=" .. value }
end

