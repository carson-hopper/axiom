-- ----- Generate packet ID header (for IDE resolution) -----------

local function generatePacketIdHeader()
    local headerPath = "src/Axiom/Network/Packet/PacketIds.generated.h"
    local headerFile = io.open(headerPath, "w")
    headerFile:write("// Auto-generated by premake5.lua — do not edit\n")
    headerFile:write("#pragma once\n\n")
    headerFile:write("#include \"Axiom/Network/Packet/PacketDirection.h\"\n")
    headerFile:write("#include \"Axiom/Network/Packet/PacketState.h\"\n\n")
    headerFile:write("#ifndef PID_UNKNOWN\n")
    headerFile:write("#define PID_UNKNOWN 65535\n")

    for version, _ in pairs(protocolVersions) do
        local packetIds = loadPacketIds(version)
        for _, definition in ipairs(packetIds) do
            local key, value = definition:match("^(.-)=(.+)$")
            if key and value then
                local fullValue = value
                if string.find(key, "_CB_", 1, true) then
                    fullValue = fullValue .. ",PacketDirection::Clientbound"
                elseif string.find(key, "_SB_", 1, true) then
                    fullValue = fullValue .. ",PacketDirection::Serverbound"
                end

                if string.find(key, "PID_HANDSHAKE", 1, true) then
                    fullValue = fullValue .. ",PacketState::Handshake"
                elseif string.find(key, "PID_STATUS_", 1, true) then
                    fullValue = fullValue .. ",PacketState::Status"
                elseif string.find(key, "PID_LOGIN_", 1, true) then
                    fullValue = fullValue .. ",PacketState::Login"
                elseif string.find(key, "PID_CONFIGURATION_", 1, true) then
                    fullValue = fullValue .. ",PacketState::Configuration"
                elseif string.find(key, "PID_PLAY_", 1, true) then
                    fullValue = fullValue .. ",PacketState::Play"
                end

                if string.find(fullValue, "-1", 1, true) then
                    fullValue = "PID_UNKNOWN," .. fullValue
                    fullValue = string.gsub(fullValue, "-1,", "")
                end

                headerFile:write("#ifndef " .. key .. "\n")
                headerFile:write("#define " .. key .. " " .. fullValue .. "\n")
                headerFile:write("#endif\n")
            end
        end
    end

    headerFile:write("#endif\n")
    headerFile:close()
end

